fn draw_marker_dot(
    canvas: &mut ImageBuffer<Rgba<u8>, Vec<u8>>,
    x: i32,
    y: i32,
    scale: u32,
    color: Rgba<u8>,
) {
    let s = scale as i32;
    for dy in 0..s {
        for dx in 0..s {
            let px = x + dx;
            let py = y + dy;
            if px < 0 || py < 0 || px >= canvas.width() as i32 || py >= canvas.height() as i32 {
                continue;
            }
            canvas.put_pixel(px as u32, py as u32, color);
        }
    }
}

fn draw_marker_text(
    canvas: &mut ImageBuffer<Rgba<u8>, Vec<u8>>,
    text: &str,
    x: i32,
    y: i32,
    scale: u32,
    color: Rgba<u8>,
) {
    let mut cx = x;
    for ch in text.chars() {
        let rows = marker_glyph(ch.to_ascii_uppercase());
        for (row_i, row) in rows.iter().enumerate() {
            for (col_i, bit) in row.chars().enumerate() {
                if bit == '1' {
                    let jitter = ((row_i + col_i) % 2) as i32;
                    draw_marker_dot(
                        canvas,
                        cx + (col_i as i32 * scale as i32) + jitter,
                        y + (row_i as i32 * scale as i32),
                        scale,
                        color,
                    );
                }
            }
        }
        cx += (6 * scale) as i32;
    }
}

pub(crate) fn render(img: &DynamicImage, shot: &ShotMeta) -> DynamicImage {
    let (w, h) = img.dimensions();
    let border_x = ((w as f32) * 0.05).round().max(14.0) as u32;
    let border_top = border_x;
    let border_bottom = ((h as f32) * 0.18).round().max(56.0) as u32;

    let out_w = w + border_x * 2;
    let out_h = h + border_top + border_bottom;
    let mut out = ImageBuffer::from_pixel(out_w, out_h, Rgba([248, 248, 244, 255]));
    imageops::overlay(&mut out, img, border_x as i64, border_top as i64);

    let caption = shot
        .app_name
        .as_ref()
        .map(|s| s.trim())
        .filter(|s| !s.is_empty())
        .unwrap_or("unknown app");
    let caption = caption.chars().take(24).collect::<String>();

    let scale = ((out_w as f32 / 220.0).round() as u32).clamp(2, 5);
    let text_w = caption.chars().count() as i32 * (6 * scale) as i32;
    let text_x = ((out_w as i32 - text_w) / 2).max(8);
    let text_y = (border_top + h + ((border_bottom.saturating_sub(7 * scale)) / 2)) as i32;
    draw_marker_text(
        &mut out,
        &caption,
        text_x,
        text_y,
        scale,
        Rgba([34, 34, 34, 255]),
    );

    DynamicImage::ImageRgba8(out)
}
